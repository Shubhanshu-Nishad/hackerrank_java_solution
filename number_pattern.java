public class number_pattern {
    public static void main(String[] args) {
        for(int i=0;i<4;i++)
        {
            int k=1;
            for(int j=0;j<=i;j++)
            {
                
                System.out.print(k+"\t");
                k++;
            }
            System.out.println();
        }
    }
}
